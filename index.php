	<script>
		$(document).ready(function(){
			$("nav ul li").click(function(){
				$("nav ul li").removeClass("active");
				$(this).addClass("active");
				var screen=$(this).attr("screen");
				$(".about-box,.skills-box,.projects-box,.contact-box").fadeOut(100);
				$("."+screen).fadeIn(100);
			});

			$("#form").submit(function(e){
				e.preventDefault();
				var name=$("#name").val();
				var mobile=$("#mobile").val();
				var message=$("#purpose").val();

				if(name=="" || mobile=="" || message=="")
				{
					alert("All Fields Are Mandatory");
				}
				else
				{
					$.ajax({
						url : "formdata.php",
						type : "POST",
						data : {name,mobile,message},
						beforeSend : function()
						{
							$("#form button").attr("disabled","disabled").html("<i class='fa fa-cog fa-spin'></i> Please Wait...");
						},
						success : function(response)
						{
							$("#form button").removeAttr("disabled").html('<i class="fa fa-paper-plane"></i> Send');
							alert(response);
						}
					});
				}
			});
		});
